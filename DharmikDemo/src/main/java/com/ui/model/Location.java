package com.ui.model;

public class Location {
	
	
	private int location_id;
	private String location_name;
	private int location_code;
	private String location_mobile_no;
	private String location_phone_no;
	private String status;
	private int createdBy;
	private String createdDate;
	private String ipAddress;
	public int getLocation_id() {
		return location_id;
	}
	public void setLocation_id(int location_id) {
		this.location_id = location_id;
	}
	public String getLocation_name() {
		return location_name;
	}
	public void setLocation_name(String location_name) {
		this.location_name = location_name;
	}
	public int getLocation_code() {
		return location_code;
	}
	public void setLocation_code(int location_code) {
		this.location_code = location_code;
	}
	public String getLocation_mobile_no() {
		return location_mobile_no;
	}
	public void setLocation_mobile_no(String location_mobile_no) {
		this.location_mobile_no = location_mobile_no;
	}
	public String getLocation_phone_no() {
		return location_phone_no;
	}
	public void setLocation_phone_no(String location_phone_no) {
		this.location_phone_no = location_phone_no;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public int getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(int createdBy) {
		this.createdBy = createdBy;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	public String getIpAddress() {
		return ipAddress;
	}
	public void setIpAddress(String ipAddress) {
		this.ipAddress = ipAddress;
	}
	
	
	
	
}
